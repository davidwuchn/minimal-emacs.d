echo "  ✅ Done"

# ═══════════════════════════════════════════════════════════════════════════
# Step 8: Register in OV5
# ═══════════════════════════════════════════════════════════════════════════
echo "[8/8] Registering in OV5 (post-init.el)..."

PROJECT_DIR_FULL="$(cd "$PROJECT_DIR" && pwd)"
POST_INIT="$OV5_DIR/post-init.el"

if [ -f "$POST_INIT" ]; then
    if ! grep -q "$PROJECT_DIR_FULL" "$POST_INIT" 2>/dev/null; then
        BLOCK=$(cat <<REGISTER
;; --- Auto-workflow: $PROJECT_NAME ($(date +%Y-%m-%d)) ---
(with-eval-after-load 'gptel-auto-workflow-projects
  (let ((proot (expand-file-name "$PROJECT_DIR_FULL/")))
    (when (file-directory-p proot)
      (add-to-list 'gptel-auto-workflow-projects (directory-file-name proot))
      (add-to-list 'gptel-auto-workflow--allowed-workspace-roots proot))))
REGISTER
)
        echo "$BLOCK" >> "$POST_INIT"
        echo "  ✅ Registered in OV5 post-init.el"
    else
        echo "  ⏭️  Already registered"
    fi
else
    echo "  ⚠️  OV5 post-init.el not found at $POST_INIT"
    echo "  Add manually: (add-to-list 'gptel-auto-workflow-projects \"$PROJECT_DIR_FULL\")"
fi

# ═══════════════════════════════════════════════════════════════════════════
# Summary
# ═══════════════════════════════════════════════════════════════════════════
echo ""
echo "=== $PROJECT_NAME initialized ==="
echo ""
echo "Next steps:"
echo "  1. cd $PROJECT_DIR"
echo "  2. Write your first module: src/$(echo "$PROJECT_NAME" | tr '[:upper:]' '[:lower:]' | tr ' ' '-')/core.clj"
echo "  3. Write tests: test/$(echo "$PROJECT_NAME" | tr '[:upper:]' '[:lower:]' | tr ' ' '-')/core_test.clj"
echo "  4. Run: ./scripts/run-tests.sh"
echo "  5. git init && git add -A && git commit -m 'Initial scaffold'"
echo "  6. Restart Emacs daemon for OV5 to discover the project"
echo ""
echo "OV5 will now run experiments on this project automatically."
