MEM

echo "  ✅ Done"

# ═══════════════════════════════════════════════════════════════════════════
# Step 7: AGENTS.md bootstrap
# ═══════════════════════════════════════════════════════════════════════════
echo "[7/8] Creating AGENTS.md..."

cat > "$PROJECT_DIR/AGENTS.md" <<AGENTS
