## Development Workflow

1. Write code in src/
2. Run ./scripts/run-tests.sh (lint + format + test)
3. Commit and push
4. OV5 picks up changes, runs experiments overnight
5. Morning: review kept experiments

## Quality Gates

| Gate | Command |
|------|---------|
| Lint | clj-kondo --lint src/ test/ |
| Format | zprint |
| Test | ./scripts/run-tests.sh |

## References

- OV5 repo: ~/.emacs.d
- Architecture: ~/.emacs.d/OUROBOROS-V5.md
- Clojure strategy: ~/.emacs.d/mementum/knowledge/clojure-first-multiplatform-architecture.md
OV5SKILL

echo "  ✅ Done"

# ═══════════════════════════════════════════════════════════════════════════
# Step 6: Mementum memory
# ═══════════════════════════════════════════════════════════════════════════
echo "[6/8] Creating mementum memory..."

cat > "$PROJECT_DIR/mementum/state.md" <<MEM
# $PROJECT_NAME — Working Memory

